    private void tblRegistrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRegistrosMouseClicked
        this.tablaRegistros = (DefaultTableModel) tblRegistros.getModel();
        int NoFils = tblRegistros.getSelectedRow();
        cerrarCeldas();
        txtId1.setText(tblRegistros.getValueAt(NoFils, 0).toString());
        txtId2.setText(tblRegistros.getValueAt(NoFils, 1).toString());
        cboTransferencia.setSelectedItem(tblRegistros.getValueAt(NoFils, 2).toString());
        cboTransferenciaIdr.setSelectedItem(tblRegistros.getValueAt(NoFils,3).toString());
        txtSaldo1.setText(tblRegistros.getValueAt(NoFils, 4).toString());
        txtSaldo2.setText(tblRegistros.getValueAt(NoFils, 5).toString());
        fils = NoFils;//ahora si utilizamos fila con valor
    }//GEN-LAST:event_tblRegistrosMouseClicked

