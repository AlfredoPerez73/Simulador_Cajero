    private void btnConsignarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConsignarMouseClicked
        if (tblRegistros.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "No se ha seleccionado Cajas","ERROR",2);
        }else{
            if (txtId1.getText().isEmpty() || cboTransferencia.equals("SELECCIONE UNA OPCION") || txtId2.getText().isEmpty() || cboTransferenciaIdr.equals("SELECCIONE UNA OPCION")){
                JOptionPane.showMessageDialog(this,"Favor no dejar campos vacios!","ERROR",2);
                txtId1.requestFocus();
            }else{
                String message[] = {"Si", "No", "Cancelar"};
                int r = JOptionPane.showOptionDialog(null, "¿Esta seguro de consignar el a la caja?", "REGISTRO DE DATOS", 0, 1, null, message, this);
                if (r == 0){

                    String[] data = new String[6];
                    data[0] = txtId1.getText();
                    data[1] = txtId2.getText();
                    data[2] = cboTransferencia.getSelectedItem().toString();
                    data[3] = cboTransferenciaIdr.getSelectedItem().toString();
                    data[4] = txtSaldo1.getText();
                    int n1 = Integer.parseInt(txtSaldo.getText());
                    int n2 = Integer.parseInt(txtSaldo1.getText());
                    String total = Integer.toString(n1+n2);
                    data[5] = total;
                    
                    for (int i = 0; i < tblRegistros.getColumnCount(); i++) {
                        tblRegistros.setValueAt(data[i], fils, i);
                    }//esto modifica la tabla

                    //modifiquemos el archivo
                    try {
                        String archivo = "REPORTE USUARIOS.txt";
                        BufferedWriter bw = new BufferedWriter(new FileWriter(archivo));
                        //recorremos la tabla
                        for (int i = 0; i < tblRegistros.getRowCount(); i++) {//barrido por fila
                            for (int j = 0; j < tblRegistros.getColumnCount(); j++) { //barrido por columna
                                  bw.write((String) (tblRegistros.getValueAt(i,j)));
                              if(j < tblRegistros.getColumnCount() -1){//agregaa separador si no es el ultimo elemento de la fila
                                   bw.write("|");
                              }

                              }
                             bw.newLine(); // se acabo las columnas, deme una nueva linea
                            }
                           bw.close();
                           
                    } catch (Exception e) {
                              JOptionPane.showMessageDialog(this,e);
                    }
                    limpiarCeldas();
                }else if(r == 1){
                }else if(r == 2){
                }
            }
        }
    }//GEN-LAST:event_btnConsignarMouseClicked
    
